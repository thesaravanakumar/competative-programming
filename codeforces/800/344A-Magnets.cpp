#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin>>n;
    int arr[n],p=0;
    for(int i=0;i<n;i++)
        cin>>arr[i];
    int a=arr[0]%10;
    for(int i=1;i<n;i++){
        if(arr[i]/10==a){
            p++;
            a=arr[i]%10;
        }
    }
    if(arr[n-1]/10!=a)p++;
    cout<<p;
    return 0;
}
