#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin>>n;
    int arr[n],p=0,sum=0;
    for(int i=0;i<n;i++){
        cin>>arr[i];
        if(p==0&&arr[i]==-1)sum++;
        else if(p>0&&arr[i]==-1)p--;
        else p+=arr[i];
    }
    cout<<sum;
    return 0;
}
