#include <bits/stdc++.h>
#define ll long long int
using namespace std;
int main()
{
    int n;
    cin >> n;
    int arr[n];
    for (int i = 0; i < n; i++)
        cin >> arr[i];
    int sum = 0, max = *max_element(arr, arr + n);
    for (int i = 0; i < n; i++)
        sum += max - arr[i];
    cout << sum;
    return 0;
}
