#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin>>t;
    while(t--){
        int n;
        cin>>n;
        int arr[2*n];
        vector<int> a;
        for(int i=0;i<2*n;i++){
            cin>>arr[i];
            if(find(a.begin(),a.end(),arr[i])==a.end())a.push_back(arr[i]);
        }
        vector<int>::iterator i;
        for(i=a.begin();i!=a.end();i++)cout<<*i<<" ";
        cout<<endl;
    }
    return 0;
}
