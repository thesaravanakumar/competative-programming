#include <bits/stdc++.h>
using namespace std;
int main()
{
    int k, n, w, sum = 0;
    cin >> k >> n >> w;
    for (int i = 1; i <= w; i++)
        sum += k * i;
    (sum-n<=0)?cout<<"0":cout << sum - n;
    return 0;
}
