#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        int arr[n], sum = 0;
        bool odd = false, even = false;
        for (int i = 0; i < n; i++)
        {
            cin >> arr[i];
            sum += arr[i];
            odd |= arr[i] % 2 != 0;
            even |= arr[i] % 2 == 0;
        }
        if (sum % 2 != 0 || (odd && even))
            cout << "YES" << endl;
        else
            cout << "NO" << endl;
    }
    return 0;
}
