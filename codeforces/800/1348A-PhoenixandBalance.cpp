#include <bits/stdc++.h>
#define ll long long int
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        int arr[n + 1];
        for (int i = 1; i <= n; i++)
            arr[i] = pow(2, i);
        int l = 0, r = 0; 
        for (int i = 1; i < n / 2; i++)
        {
            l += arr[i];
        }
        l += arr[n];
        for (int i = n / 2; i < n; i++)
        {
            r += arr[i];
        }
        cout << l - r << endl;
    }
    return 0;
}
