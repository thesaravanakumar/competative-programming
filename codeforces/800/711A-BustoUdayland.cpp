#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    string a, b;
    while (n--)
    {
        cin >> a;
        b += a + "\n";
    }
    int q = b.find("OO");
    if (q >= 0)
    {
        b.replace(q, 2, "++");
        cout << "YES" << endl << b;
    }
    else
        cout << "NO";
    return 0;
}
