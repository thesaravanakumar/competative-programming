#include <bits/stdc++.h>
using namespace std;
int main()
{
    long long int n, temp = 0;
    cin >> n;
    while (n)
    {
        if (n % 10 == 4 || n % 10 == 7)
        {
            temp++;
        }
        n /= 10;
    }
    if (temp == 4 || temp == 7)
        cout << "YES";
    else
        cout << "NO";
    return 0;
}
