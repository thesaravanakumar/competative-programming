#include <bits/stdc++.h>
using namespace std;
int main()
{
    int x;
    cin >> x;
    int count = 0;
    for (int i = 5; i >= 1; i--)
    {
        count += x / i;
        x %= i;
    }
    cout << count;
    return 0;
}
