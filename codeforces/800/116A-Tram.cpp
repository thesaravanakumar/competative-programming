#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n, temp = 0, maxpass = 0;
    cin >> n;
    while (n--)
    {
        int in, out;
        cin >> out >> in;
        maxpass += in - out;
        if (temp < maxpass)
            temp = maxpass;
    }
    cout << temp;
    return 0;
}
