#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n,sum=0,i=1,j=2;
    cin>>n;
    while(n){
        if(n<i)break;
        else {n=n-i;sum++;}
        i+=j;
        j++;
    }
    cout<<sum;
    return 0;
}
