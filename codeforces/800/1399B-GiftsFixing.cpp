#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        vector<int> a(n), b(n);
        for (int i = 0; i < n; i++)
            cin>>a[i];
        for (int i = 0; i < n; i++)
            cin>>b[i];
        int min1 = *min_element(a.begin(), a.end());
        int min2 = *min_element(b.begin(), b.end());
        long long int sum=0;
        for(int i=0;i<n;i++)sum+=max(a[i]-min1,b[i]-min2);
        cout << sum << endl;
    }
    return 0;
}
