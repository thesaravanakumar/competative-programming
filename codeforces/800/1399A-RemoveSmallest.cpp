#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        int arr[n], temp = 0;
        for (int i = 0; i < n; i++)
            cin >> arr[i];
        sort(arr,arr+n);
        for (int i = 1; i < n; i++)
        {
            if (arr[i] - arr[i - 1] > 1)
            {
                temp = 1;
                break;
            }
        }
        (temp == 0) ? cout << "YES" << endl : cout << "NO" << endl;
    }
    return 0;
}
