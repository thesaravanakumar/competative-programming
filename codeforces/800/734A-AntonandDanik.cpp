#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    char a[n];
    cin >> a;
    int an = 0, da = 0;
    for (int i = 0; i < n; i++)
        if (a[i] == 'A')
            an++;
        else
            da++;
    if (an > da)
        cout << "Anton";
    else if (da > an)
        cout << "Danik";
    else
        cout << "Friendship";
    return 0;
}
